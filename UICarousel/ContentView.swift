import SwiftUI

struct ContentView: View {
    var body: some View {
        GeometryReader { reader in
            CarouselView(size: reader.size)
        }
        .background(Color(white: 0.15, opacity: 1).ignoresSafeArea())

    }
}

struct CarouselView: View {
    let colors = [Color.red, .blue, .orange, .purple, .pink, .green, .yellow]
    let emojis = emojisMock.shuffled()

    let size: CGSize
    private let padding = CGFloat(20)
    let damping: CGFloat = 8

    var body: some View {
        ScrollView(.horizontal) {
            LazyHStack {
                ForEach(emojis.indices, id: \.self) { i in
                    let emoji = emojis[i]
                    let color = colors[i % colors.count]

                    item(emoji: emoji, color: color)
                }
            }
            .padding(.horizontal, padding)
        }
    }
    @ViewBuilder
    private func item(emoji: String, color: Color) -> some View {
        let width: CGFloat = size.width - padding * 2

        GeometryReader { reader in
            ZStack {
                RoundedRectangle(cornerRadius: 20)
                    .foregroundColor(color)

                Text(emoji)
                    .font(.system(size: 90))
            }
            .rotation3DEffect(getRotationAngle(reader: reader),
                              axis: (x: 0.0, y: 1.0, z: 0.0))
        }
        .frame(width: width, height: width * 1.4)
    }

    func getRotationAngle(reader: GeometryProxy) -> Angle {
        let midX = reader.frame(in: .global).midX
        let degrees = Double(midX - size.width / 2) / damping

        return Angle(degrees: -degrees)
    }
}

